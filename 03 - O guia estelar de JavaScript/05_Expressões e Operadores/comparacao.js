//Operadores de comparação

//Irá comprar valores e retornar um boolean como resposta à comparação

let one = 1
let two = 2

// == igual a 
console.log(two == 1)
console.log(one == "1")

// != diferente a 
console.log(one != 2)
console.log(two != 2)
console.log(two != '2')

//Estritamente igual a
console.log(one === '1')
console.log(one !== '1')

//Estritamente diferente a
console.log(two !== '2')
console.log(two !== 2)

console.log("-----------------------")


//maior que >
console.log(one > two)
console.log(two > one)

//maior igual a >=
console.log(one >= 1)
console.log(one >= 2)

